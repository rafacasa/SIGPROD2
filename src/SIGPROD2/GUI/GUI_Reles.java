    private void avancarTela() {
        while (true && this.getSelected() < this.tabelaTab.getComponentCount()) {
            int selecionado = this.getSelected() + 1;

            this.tabelaTab.setSelectedIndex(selecionado);
            if (this.tabelaTab.isEnabledAt(selecionado)) {
                this.selecionaCheckBox(selecionado);
                break;
            }
        }
    }

    private void retornarTela() {
        while (true && this.getSelected() > 0) {
            int selecionado = this.getSelected() - 1;

            this.tabelaTab.setSelectedIndex(selecionado);
            if (this.tabelaTab.isEnabledAt(selecionado)) {
                break;
            }
        }
    }

    private void selecionaCheckBox(int selecionado) {
        System.out.println(selecionado);
        switch (selecionado) {
            case 1 :
                if (this.temporizadaFase.getSelectedIndex() == 0) {
                    this.faseCorrentePassoConstante.setSelected(true);
                    this.faseCorrentePassoConstanteActionPerformed(null);
                } else {
                    this.faseCurvaFormasActionPerformed(null);
                }
                break;
            case 2 :
                this.faseInstantaneaPassoCorrente.setSelected(true);
                this.faseInstantaneaPassoCorrenteActionPerformed(null);
                break;
            case 3 :
                if (this.temporizadaNeutro.getSelectedIndex() == 0) {
                    this.neutroCorrentePassoConstante.setSelected(true);
                    this.neutroCorrentePassoConstanteActionPerformed(null);
                } else {
                    this.neutroCurvaFormasActionPerformed(null);
                }
            case 4 :
                this.neutroInstantaneaPassoCorrente.setSelected(true);
                this.neutroInstantaneaPassoCorrenteActionPerformed(null);
        }
    }

    private void selecionaDadosGerais() {
        this.unidadeInstantaneaFase.setSelected(true);
        this.unidadeInstantaneaNeutro.setSelected(true);
        this.unidadeTemporizadaFase.setSelected(true);
        this.unidadeTemporizadaNeutro.setSelected(true);
    }

    private void disablePanels() {
        boolean faseTemporizada = this.unidadeTemporizadaFase.isSelected();
        boolean faseInstantanea = this.unidadeInstantaneaFase.isSelected();
        boolean neutroTemporizada = this.unidadeTemporizadaNeutro.isSelected();
        boolean neutroInstantanea = this.unidadeInstantaneaNeutro.isSelected();

        this.tabelaTab.setEnabledAt(1, faseTemporizada);
        this.tabelaTab.setEnabledAt(2, faseInstantanea);
        this.tabelaTab.setEnabledAt(3, neutroTemporizada);
        this.tabelaTab.setEnabledAt(4, neutroInstantanea);
    }

    private int getSelected() {
        return this.tabelaTab.getSelectedIndex();
    }

